package Sesi5;

public class DeretAngka {
    public static void main(String[] args) {
        // Melakukan perulangan dari 1 hingga 5
        for (int i = 1; i <= 5; i++) {
            // Menampilkan angka saat ini
            System.out.println("Iterasi ke-" + i + ": Angka = " + i);
        }
    }
}
