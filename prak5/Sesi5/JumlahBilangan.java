package Sesi5;

public class JumlahBilangan {
    public static void main(String[] args) {
        int jumlah = 0; 

        
        for (int i = 1; i <= 100; i++) {
            jumlah += i;
            
        }

        
        System.out.println("Jumlah bilangan dari 1 hingga 100 adalah: " + jumlah);
    }
}
